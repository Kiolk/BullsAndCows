package com.example.notepad.bullsandcows.data.models;

public class UserMoveModel {

    private String mMove;

    private String mInputNumber;

    private String mBulls;

    private String mCows;

    public String getMove() {
        return mMove;
    }

    public void setMove(String pMove) {
        mMove = pMove;
    }

    public String getInputNumber() {
        return mInputNumber;
    }

    public void setInputNumber(String pInputNumber) {
        mInputNumber = pInputNumber;
    }

    public String getBulls() {
        return mBulls;
    }

    public void setBulls(String pBulls) {
        mBulls = pBulls;
    }

    public String getCows() {
        return mCows;
    }

    public void setCows(String pCows) {
        mCows = pCows;
    }
}
